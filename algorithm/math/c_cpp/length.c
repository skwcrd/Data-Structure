// Converters Kilometers to Miles.
double kilo_to_mile(double km)
{
    return km * 0.621371;
}

// Converters Miles to Kilometers.
double mile_to_kilo(double mile)
{
    return mile * 1.609344;
}

// Converters Centimeters to Inches.
double centi_to_inche(double cm)
{
    return cm * 0.393701;
}

// Converters Centimeters to Feet.
double centi_to_feet(double cm)
{
    return cm * 0.032808;
}

// Converters Centimeters to Yards.
double centi_to_yard(double cm)
{
    return cm * 0.010936;
}